#include "main.h"
/**
 * print_to_98 - This is a function
 * @n: This is the parameter
 * Return: Always l and n
 */
void print_to_98(int n)
{
if (n <= 98)
{
while (n <= 98)
{
return (n);
_putchar(',');
_putchar(' ');
n++;
}
}
else
{
while (n >= 98)
{
return (n);
_putchar(',');
_putchar(' ');
n--;
}
}
}
